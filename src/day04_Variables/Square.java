package day04_Variables;

public class Square {
    public static void main(String[] args) {
        int side = 4;

        int area = side * side;
        int perimetr = 4 * side;

        System.out.println("side = " + side + " inch");
        System.out.println("area = " + area  + " inch");
        System.out.println("perimetr = " + perimetr  + " inch");
    }
}
