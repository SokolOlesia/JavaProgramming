package day04_Variables;

public class Circle {
    public static void main(String[] args) {
        double radius = 3.6;
        double pi = 3.14;
        double area = pi * (radius * radius);
        double perimeter = 2 * pi * radius;

        System.out.println("area = " + area);
        System.out.println("area = " + area);
        System.out.println("perimeter = " + perimeter);
    }
}
