package day03_EscapeSequence;
/*
\n - strats a new line
\t - tab button(paragraph space)
\\ - backslash
\" -to print double quote
 */
public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Java \nPythoc \nC#"); //starts a  new line
        System.out.println("Java \tPythoc \tC#");
    }
}
