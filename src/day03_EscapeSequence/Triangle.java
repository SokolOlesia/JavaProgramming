package day03_EscapeSequence;

public class Triangle {
    public static void main(String[] args) {
        System.out.println("     ^");
        System.out.println("    / \\ ");
        System.out.println("   /   \\ ");
        System.out.println("  /     \\ ");
        System.out.println(" /       \\ ");
        System.out.println("/         \\ ");
        System.out.println("-----------");
    }
}
