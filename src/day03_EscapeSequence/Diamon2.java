package day03_EscapeSequence;

public class Diamon2 {
    public static void main(String[] args) {
        System.out.println("     ^");
        System.out.println("    / \\ ");
        System.out.println("   / * \\ ");
        System.out.println("  / * * \\ ");
        System.out.println(" / * * * \\ ");
        System.out.println(" ---------");
        System.out.println(" \\ * * * /");
        System.out.println("  \\ * * /");
        System.out.println("   \\ * /");
        System.out.println("    \\ /");
        System.out.println("     v");
    }
}
